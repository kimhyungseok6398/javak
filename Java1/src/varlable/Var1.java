package varlable;

public class Var1 {
	public static void main(String[] args) {
		//변수 (프로그래밍) 저장공간 
		String var1 = "java";//var라는 공간을 생성 java라는 데이터를 넣음
		
		System.out.println("Hello "+var1+"!");
		
		System.out.println("안녕하세요 등등");
		
		System.out.println("34-2");
		//개벌자 요건이 32인지 34-2 인지 모름 
		//사용자의 의도를 판단 하기 위해 변수의 자료형 이라는 개념을 추가
		
	}

}
