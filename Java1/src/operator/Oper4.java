package operator;

public class Oper4 {
	public static void main(String[] args) {
		int x =20;
		int y =20;
		boolean result;
		result = x > y;
		System.out.println("x > y = "+result);
		result = x < y;
		System.out.println("x > y = "+result);
		result = x >= y;
		System.out.println("x > y = "+result);
		result = x == y;
		System.out.println("x > y = "+result);
		result = x != y;
		System.out.println("x > y = "+result);
		
	}

}
