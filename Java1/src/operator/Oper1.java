package operator;

public class Oper1 {
	public static void main(String[] args) {
		//프로그래밍은 위에서 아래 왼쪽에서 오른쪽 진행
		//대입연산자 = : 오른쪽에 있는 값을 왼쪽 공간에 담아라
	int 숫자1 = 3;
	int 숫자2 = 2;
	int 숫자3 = 1;
	System.out.println(숫자1-숫자2*숫자2);
	
	//뺴기를 먼저하고 싶으면 괄호 사용
	
	//지금 자료형이 int 이기때문에 
	//나누기를 하면 소수점 삭제 
	System.out.println(3/2);
	
	System.out.println(3/2.0);

	}
}
