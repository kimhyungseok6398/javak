package condition;

public class Switch1 {
	public static void main(String[] args) {
		int num =0;
		System.out.println("숫자를 입력하세요");
		switch (num) {
		case 1 : 
			System.out.println("안녕하세요");
			break;
		case 2 :
			System.out.println("반갑습니다");
			break;
		case 3 :
			System.out.println("잘있어요");
		   
		}
	}
}
			
			
			
			
			
	
		
		
	
